    {
        Task<T> GetByIdAsync(Guid id);
        Task<IReadOnlyList<T>> ListAllAsync();
        Task<T> AddAsync(T entity);
        Task updateAsync(T entity);
        Task DeleteAsync(T entity);


    }
}
